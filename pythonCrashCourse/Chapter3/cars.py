cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort()
print(cars)

cars.sort(reverse=True)
print(cars)

# The sorted() function allows you to temporarily sort the cars
cars = ['bmw', 'audi', 'toyota', 'subaru']
print(sorted(cars))
print(cars)

# Use the reverse method to revers a list order
cars = ['bmw', 'audi', 'toyota', 'subaru']
print(cars)
cars.reverse()
print(cars)

# Find the length of a list using the len(function)
cars = ['bmw', 'audi', 'toyota', 'subaru']
print(len(cars))